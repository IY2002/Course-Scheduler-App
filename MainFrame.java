    private void ScheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleCourseSubmitButtonActionPerformed
        // TODO add your handling code here:
        String courseStatus = "";
        StudentEntry studentScheduled = (StudentEntry)StudentNameComboBox_ScheduleCourse.getSelectedItem();
        CourseEntry courseScheduled = (CourseEntry) SelectCourseComboBox.getSelectedItem();
        if(CourseQueries.getCourseSeats(currentSemester, courseScheduled.getCourseCode()) > ScheduleQueries.getScheduledStudentCount(currentSemester, courseScheduled.getCourseCode(), "S")){
            courseStatus = "S";
            ScheduleCourseOutput.setText(studentScheduled.getOutputString() + " has been scheduled for " + courseScheduled.getCourseCode());
        }
        else{
            courseStatus = "W";
            ScheduleCourseOutput.setText(studentScheduled.getOutputString() + " has been waitlisted for " + courseScheduled.getCourseCode());
        }
        ScheduleEntry newClass = new ScheduleEntry(currentSemester, (String)courseScheduled.getCourseCode(),(String) studentScheduled.getStudentID(), courseStatus, new java.sql.Timestamp(System.currentTimeMillis()));
        ScheduleQueries.addScheduleEntry(newClass);
        rebuildCourseComboBoxes();
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_ScheduleCourseSubmitButtonActionPerformed

