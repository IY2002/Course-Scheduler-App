    private void DropCourseButton_DropCourse_AdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropCourseButton_DropCourse_AdminActionPerformed
        // TODO add your handling code here:
        DropCourse_AdminOutputPanel.setText("");
        
        // setup the output field text by adding onto a base string all the students that were dropped
        String droppedScheduledStudentsMessage = "Scheduled students dropped from the course:\n";
        String droppedWaitlistedStudentsMessage = "Waitlisted students dropped from the course:\n";

        StudentEntry currentStudent;
        for (ScheduleEntry currentEntry : ScheduleQueries.getScheduledStudentsByCourse(currentSemester,((CourseEntry)DropCourseComboBox_DropCourse_Admin.getSelectedItem()).getCourseCode())){
            currentStudent = StudentQueries.getStudent(currentEntry.getStudentID());
            droppedScheduledStudentsMessage += currentStudent.getOutputString() + "\n";
        }
        for (ScheduleEntry currentEntry : ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, (String)DropCourseComboBox_DropCourse_Admin.getSelectedItem())){
            currentStudent = StudentQueries.getStudent(currentEntry.getStudentID());
            droppedWaitlistedStudentsMessage += currentStudent.getOutputString() + "\n";
        }
        
        ScheduleQueries.dropScheduleByCourse(currentSemester, (String) DropCourseComboBox_DropCourse_Admin.getSelectedItem());
        CourseQueries.dropCourse(currentSemester, (String) DropCourseComboBox_DropCourse_Admin.getSelectedItem());
        
        DropCourse_AdminOutputPanel.setText(droppedScheduledStudentsMessage + "\n" + droppedWaitlistedStudentsMessage);
        rebuildCourseComboBoxes();
    }//GEN-LAST:event_DropCourseButton_DropCourse_AdminActionPerformed

    private void DropStudentButton_DropStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentButton_DropStudentActionPerformed
        // TODO add your handling code here:
        DropStudentOutputTextArea.setText("");
        
        StudentEntry currentStudent = StudentQueries.getStudent(((StudentEntry)StudentNameComboBox_DropCourse_Student.getSelectedItem()).getStudentID());
        DropStudentOutputTextArea.append(((StudentEntry)StudentNameComboBox_DropCourse_Student.getSelectedItem()).getOutputString() + " has been dropped from the list of students.\n");

        
        for (String currentSem : SemesterQueries.getSemesterList()){
            
            String studentDroppedCourses = "";
            String WaitlistedStudentScheduled = "";
            
            for(ScheduleEntry currentSchedule : ScheduleQueries.getScheduleByStudent(currentSem, currentStudent.getStudentID())){
                ScheduleQueries.dropStudentScheduleByCourse(currentSem, currentStudent.getStudentID(), currentSchedule.getCourseCode());
                if(currentSchedule.getStatus().toLowerCase().equals("w")){
                    studentDroppedCourses += ((StudentEntry)SelectStudentComboBox_DropStudent.getSelectedItem()).getOutputString() + " has been dropped from the waitlist for " + currentSchedule.getCourseCode() + "\n";
                }
                else{
                    studentDroppedCourses += ((StudentEntry)SelectStudentComboBox_DropStudent.getSelectedItem()).getOutputString() + " has been dropped from " + currentSchedule.getCourseCode() + "\n";
                    ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSem, currentSchedule.getCourseCode());

                    if(waitlistedStudents.size() >= 1){
                        ScheduleQueries.updateScheduleEntry(currentSem, waitlistedStudents.get(0));
                        StudentEntry unwaitlistedStudent = StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID());
                        WaitlistedStudentScheduled += unwaitlistedStudent.getOutputString() + " has been scheduled into " + waitlistedStudents.get(0).getCourseCode() + "\n";
                    }
                }
                if (!studentDroppedCourses.isEmpty() || !WaitlistedStudentScheduled.isEmpty()){
                    DropStudentOutputTextArea.append("For Semester: " + currentSem + "\n");
                    DropStudentOutputTextArea.append(studentDroppedCourses);
                    DropStudentOutputTextArea.append(WaitlistedStudentScheduled);
                }
            }
        }
        
        StudentQueries.dropStudent(((StudentEntry)SelectStudentComboBox_DropStudent.getSelectedItem()).getStudentID());
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_DropStudentButton_DropStudentActionPerformed

